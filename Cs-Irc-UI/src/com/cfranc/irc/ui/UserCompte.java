	private class Connecter extends AbstractAction {

		/**
		 * 
		 */
		private static final long serialVersionUID = 1L;

		public void actionPerformed(ActionEvent e) {
			
			if (mode == 1) {

				// Vérifier les saisies
				if (txtPwd.getText().equals("") ) {
					JOptionPane.showMessageDialog(null,
							"Vous devez saisir un Login.", "Inane error",
							JOptionPane.ERROR_MESSAGE);
					return;
				}

				userConnect = new UserImpl(txtLogin.getText(), txtPwd.getText(), txtNom.getText(), txtPrenom.getText(), chemin);
				userConnect.enregistrer();
				
				// Charger le compte de l'utilisateur
				userConnect.charger(userConnect.getLogin(), userConnect.getPwd());
				System.out.println("user charg");

				
			} 

			setVisible(false);
		}

	}

