	private void btnSelectionerImageActionPerformed(ActionEvent evt) {
		
		
		JFileChooser chooser = new JFileChooser();
		FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG & GIF Images", "jpg", "gif");
		chooser.setFileFilter(filter);
		int returnVal = chooser.showOpenDialog(null);
		if (returnVal == JFileChooser.APPROVE_OPTION) {
			chemin = chooser.getSelectedFile().toString();
			affecterImage(chemin);
		}
	}

	/**
	 * 
	 */
	private void affecterImage(String chemin) {
		
		File f = new File(chemin);
		
		if (f.exists() && !f.isDirectory()) {
			ImageIcon img = new ImageIcon(chemin);
			Image img2 = img.getImage().getScaledInstance(200, 200, Image.SCALE_SMOOTH);
			jLabel7.setIcon(new ImageIcon(img2));
			jLabel7.repaint();
			pnlImage.repaint();
			this.repaint();
		}
	}

